
public class User {
	
	private String name;
	private String dob;
	private char gender;
	private String address;
	private String phone;
	private String email;
	private String selfAssessmentResult;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public char getGender() {
		return gender;
	}
	public void setGender(char gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSelfAssessmentResult() {
		return selfAssessmentResult;
	}
	public void setSelfAssessmentResult(String selfAssessmentResult) {
		this.selfAssessmentResult = selfAssessmentResult;
	}
	
	

}
